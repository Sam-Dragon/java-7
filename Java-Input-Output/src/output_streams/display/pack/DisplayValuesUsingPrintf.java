package output_streams.display.pack;

public class DisplayValuesUsingPrintf {

	public static void main(String[] args) {

		int n = 100;
		float f = 100;
		String s = "Rahul Sheth";
		char c = 'M';
		boolean b = true;

		System.out.printf("Integer n = %d, Float f = %f,  String s = %s, Character c = %c, Boolean b = %b", n, f, s, c,
				b);
	}
}
